    private class ModeloTabelaResultadoAveriguacao extends AbstractTableModel {
        @Override
        public String getColumnName(int coluna) {
            if (coluna == 0) {
                return "NIS:";
            }  else if (coluna == 1) {
                return "Beneficiario:";
            } else if (coluna == 2){                
                return "Data do Pedido:";
            }else if (coluna == 3){                
                return "Situacao:";
            } else if (coluna == 4){                
                return "Situacao:";
            } else if (coluna == 5){ 
                return "Data do Resultado:";
            }else if (coluna == 5){ 
                return "Resultado:";
            }else{
                return "Decisao:";
            }
        }

        @Override
        public int getRowCount() {
            return listaResultadoAveriguacao.size();
        }

        @Override
        public int getColumnCount() {
            return 7;
        }

        @Override
        public Object getValueAt(int linha, int coluna) {
            ResultadoAveriguacao resultadoAveriguacao= listaResultadoAveriguacao.get(linha);
            if (coluna == 0) {
                return resultadoAveriguacao.getPedidoAveriguacao().getBeneficiario().getNis();
            } else if (coluna == 1) {
                return resultadoAveriguacao.getPedidoAveriguacao().getBeneficiario().getNome();
            } else if (coluna == 2){ 
                SimpleDateFormat formatador= new SimpleDateFormat("dd/MM/yyyy");
                return formatador.format(resultadoAveriguacao.getPedidoAveriguacao().getDataPedido());
            } else if (coluna == 3){
                return resultadoAveriguacao.getPedidoAveriguacao().getSituacao();
            }else if (coluna == 4){
                SimpleDateFormat formatador= new SimpleDateFormat("dd/MM/yyyy");
                return formatador.format(resultadoAveriguacao.getDataResultado());
            }else if (coluna == 5){
                return resultadoAveriguacao.getResultado();
            }else{
                return resultadoAveriguacao.getDecisao();
            }
        }

    }

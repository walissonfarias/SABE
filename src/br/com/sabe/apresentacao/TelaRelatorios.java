    private void recuperarCamposTela() throws ParseException {
        if(txtDataInicio != null && txtDataTermino != null){
            SimpleDateFormat formatador = new SimpleDateFormat("dd/MM/yyyy");
            this.dataInicio = formatador.parse(txtDataInicio.getText().trim());
            this.dataTermino = formatador.parse(txtDataTermino.getText().trim());
        }else{
            throw new CampoObrigatorioException();
        }          
    }
    public void gerarRelatorioLocalidadesVisitadas() throws SQLException{
        try{
            String arquivoRelatorio = System.getProperty("user.dir");
            arquivoRelatorio += "/relatorios/LocalidadesVisitadas.jasper";
            
            Map<String, Object> parametros = new HashMap<String, Object>();
            
            JRBeanCollectionDataSource fonteDados = new JRBeanCollectionDataSource(this.listaLocalidadesVisitadas);
            
            JasperPrint relatorioGerado = JasperFillManager.fillReport(arquivoRelatorio, parametros, fonteDados);
                            
            JasperViewer telaExibicaoRelatorio = new JasperViewer(relatorioGerado, false);
            telaExibicaoRelatorio.setTitle("Relatório de Localidades Visitadas");
            telaExibicaoRelatorio.setVisible(true);                        
        }catch(JRException e){
            JOptionPane.showMessageDialog(this, 
                    "Erro ao exibir relatório.",
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }      
    }

    private void btnGerarRelatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGerarRelatorioActionPerformed
        try{
            if(rdoLocalidadesVisitadas.isSelected()){
                ResultadoAveriguacaoBO resultadoAveriguacaoBO = new ResultadoAveriguacaoBO();
                this.recuperarCamposTela();
                this.listaLocalidadesVisitadas = resultadoAveriguacaoBO.buscarLocalidadesVisitadas(this.dataInicio, this.dataTermino);
                this.gerarRelatorioLocalidadesVisitadas();
            }else if(rdoSistuacaoBeneficiarios.isSelected()){     
                PedidoAveriguacaoBO pedidoAveriguacaoBO= new PedidoAveriguacaoBO();
                this.recuperarCamposTela();
                this.listaSituacaoBeneficiarios = pedidoAveriguacaoBO.buscarSituaçõesBeneficiarios(this.dataInicio, this.dataTermino);                                        
                this.gerarRelatorioLocalidadesVisitadas();
            }else{
                throw new CampoObrigatorioException("Escolha um tipo de Relatorio!");
            }
        }catch(SistemaAveriguacaoException sae){
            JOptionPane.showMessageDialog(this, 
                "Erro ao exibir relatório.",
                sae.getMessage(), JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(TelaRelatorios.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException px) {
            Logger.getLogger(TelaRelatorios.class.getName()).log(Level.SEVERE, null, px);
        }        
    }//GEN-LAST:event_btnGerarRelatorioActionPerformed

